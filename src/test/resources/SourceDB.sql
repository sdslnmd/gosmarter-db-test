DROP TABLE "USERITEMHISTORY" IF EXISTS;
DROP TABLE "USER" IF EXISTS;
DROP TABLE "ITEM" IF EXISTS;
DROP TABLE "USERFRIEND" IF EXISTS;

CREATE TABLE "USER" (
  "ID" INT NOT NULL PRIMARY KEY,
  "FACEBOOKID" VARCHAR(45) NULL ,
  "AGE" DECIMAL(10,0) NULL ,
  "CURRENTLOCATION" VARCHAR(45) NULL ,
  "ORIGIN" VARCHAR(45) NULL ,
  "LUXURY" BOOLEAN NULL ,
  "LATEST" BOOLEAN NULL ,
  "GENDER" VARCHAR(45) NULL
);

CREATE TABLE "USERFRIEND" (
  "ID" INT NOT NULL PRIMARY KEY,
  "USERID" INT NULL ,
  "FRIENDID" INT NULL ,
  constraint USER_FK foreign key (USERID)
  references "USER"(ID),
  constraint FRIEND_FK foreign key (FRIENDID)
  references "USER"(ID)
);

CREATE TABLE "ITEM" (
  "ID" INT NOT NULL PRIMARY KEY,
  "NAME" VARCHAR(45) NULL ,
  "LINK" VARCHAR(45) NULL ,
  "CATEGORY" VARCHAR(45) NULL ,
  "BRAND" VARCHAR(45) NULL ,
  "PRICE" DECIMAL(10,0) NULL 
);

CREATE  TABLE "USERITEMHISTORY" (
  "ID" INT NOT NULL PRIMARY KEY,
  "USERID" INT NULL ,
  "ITEMID" INT NULL ,
  "LIKEDDATE" DATE NULL ,
  constraint USER_FK1 foreign key (USERID)
  references "USER"(ID),
  constraint ITEM_FK foreign key (ITEMID)
  references "ITEM"(ID)
);

